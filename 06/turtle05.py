import turtle
turtle.shape("turtle")


for i in range(3):
    turtle.pencolor("red")
    turtle.pensize(3)
    turtle.forward(100)
    turtle.left(120)

for i in range(4):
    turtle.pencolor("blue")
    turtle.pensize(7)
    turtle.forward(100)
    turtle.right(90)

turtle.done()

